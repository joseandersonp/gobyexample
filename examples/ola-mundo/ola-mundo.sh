# Para executar o programa, salve o código 
# em `ola-mundo.go` e use `go run`.
$ go run ola-mundo.go
Olá mundo

# Às vezes, queremos compilar nossos programas em 
# arquivos binários. Podemos fazer isso usando `go build`.
$ go build ola-mundo.go
$ ls
ola-mundo	ola-mundo.go

# Podemos, então, executar o arquivo binário diretamente.
$ ./ola-mundo
Olá mundo

